package aug2025;

public class BeginnerVariable {
        public static void main(String[] args) {
            // 1. Declare variables
            String name = "Rahul";
            int age = 20;
            double height = 5.9;
            boolean isProgrammer = true;

            // 2. Print them
            System.out.println("Name: " + name);
            System.out.println("Age: " + age);
            System.out.println("Height: " + height + " feet");
            System.out.println("Is a Programmer? " + isProgrammer);
        }
    }

